//for, while, do while

console.log("FOR LOOP....");
for (let i = 1; i <= 10; i++) {
    console.log(5 * i);
}

console.log("WHILE LOOP....");
let i = 1;
while (i < 10) {
    console.log(5 * i);
    i++;
}

console.log("DO WHILE LOOP....");
i = 1;
do {
    console.log(5 * i);
    i++;
} while (i < 10);